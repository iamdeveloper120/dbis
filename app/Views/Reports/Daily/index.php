                attr: {
                    id: 'btn_generate_report'
                },
                action: function() {
                    startGenerate('');
                }
            });
        }

        table = $('#daily_report_datatable').DataTable({
            ajax: {
                url: '<?= base_url('reports/daily/data') ?>',
                type: 'POST',
                data: function(d) {
                    d.subject_id = $('#client_dropdown_list').val();
                    d.start_date = '';
                    d.end_date = '';
                },
                dataSrc: function(json) {
                    return (json && json.data) ? json.data : [];
                }
            },
            lengthChange: false,
            layout: {
                topStart: {
                    buttons: buttonsConfig
                },
                topEnd: {
                    search: {
                        placeholder: 'Search'
                    }
                }
            },
            order: [[0, 'desc']],
            columns: [
                {
                    data: 'report_date',
                    render: function(data, type, row) {
                        if (!data) return '';
                        if (type === 'sort' || type === 'type') {
                            return moment(data, 'YYYY-MM-DD').format('YYYYMMDD');
                        }
                        return row.report_date_display || moment(data, 'YYYY-MM-DD').format(momentDateFormat);
                    }
                },
                {
                    data: null,
                    render: function(data, type, row) {
                        return row.latest_version_id ? ('v' + row.latest_version_no) : '-';
                    }
                },
                {
                    data: 'latest_status',
                    render: function(data) {
