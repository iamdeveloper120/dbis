            Swal.fire({
                title: 'Active Draft Exists',
                text: 'An active Daily Report draft already exists' + (draftDate ? (' for ' + draftDate) : '') + (versionNo ? (' (v' + versionNo + ')') : '') + '. Open it now?',
                icon: 'info',
                showCancelButton: true,
                confirmButtonText: 'Open Draft',
                cancelButtonText: 'Close',
                customClass: {
                    confirmButton: 'btn btn-primary w-xs me-2 mt-2',
                    cancelButton: 'btn btn-secondary w-xs mt-2'
                },
                buttonsStyling: false
            }).then(function(result) {
                if (result.isConfirmed && draftUrl) {
                    withCurrentListState(draftUrl).then(function(nextUrl) {
                        window.location.href = nextUrl;
                    });
                }
            });
        }

