                                menu += '<li><a class="dropdown-item open-draft-link" href="#" data-version-id="' + row.latest_version_id + '"><i class="ri-draft-line align-bottom me-2 text-muted"></i>Open Active Draft</a></li>';
                            } else if (row.latest_status === 'FINAL') {
                                menu += '<li><a class="dropdown-item regenerate-version" href="#" data-version-id="' + row.latest_version_id + '" data-report-date="' + (row.report_date || '') + '"><i class="ri-refresh-line align-bottom me-2 text-muted"></i>Regenerate Draft</a></li>';
                            }
