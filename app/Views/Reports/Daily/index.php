        var table = null;
        var stateTokenUrl = '<?= base_url('reports/daily/state-token') ?>';
        var initialListState = <?= json_encode($initial_state ?? ['client_id' => '', 'start_date' => '', 'end_date' => '', 'dt_page' => 0], JSON_UNESCAPED_UNICODE) ?>;
