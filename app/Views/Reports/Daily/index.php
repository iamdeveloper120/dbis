        function validateSelection() {
            var clientId = $('#client_dropdown_list').val();
            if (!clientId) {
                showAlert('', 'Select Client', 'error');
                return false;
            }
            return true;
        }

        function reloadTableWithLoader(resetPaging) {
            showPageLoader();
            table.ajax.reload(function() {
                hidePageLoader();
            }, resetPaging);
        }

        function postWithLoader(url, payload) {
            showPageLoader();
            return $.post(url, payload).always(function() {
                hidePageLoader();
            });
        }

        function runGenerate(clientId, reportDate) {
            postWithLoader('<?= base_url('reports/daily/generate') ?>', {
                subject_id: clientId,
                report_date: reportDate
            }).done(function(response) {
                if (response.status !== 'success') {
                    if (response.statusText === 'ActiveDraftExists') {
                        handleActiveDraftResponse(response);
                        return;
                    }
                    showAlert(response.statusText || '', response.message || 'Unable to generate draft.', response.status || 'error');
                    return;
                }

                $('#generate_report_modal').modal('hide');
                showAlert(response.statusText, response.message, response.status);
                reloadTableWithLoader(false);
                if (response.data && response.data.draft_url) {
                    withCurrentListState(response.data.draft_url).then(function(nextUrl) {
                        window.location.href = nextUrl;
                    });
                }
            }).fail(function(jqXHR, textStatus, error) {
                showAlert(jqXHR.status, 'Request failed: ' + textStatus + '<br>' + error, 'error');
            });
        }

        function runRegenerate(versionId) {
            postWithLoader('<?= base_url('reports/daily/version') ?>/' + versionId + '/regenerate', {})
                .done(function(response) {
                    if (response.status !== 'success') {
                        if (response.statusText === 'ActiveDraftExists') {
                            handleActiveDraftResponse(response);
                            return;
                        }
                        showAlert(response.statusText || '', response.message || 'Unable to regenerate draft.', response.status || 'error');
                        return;
                    }

                    showAlert(response.statusText || '', response.message || 'Draft regenerated.', response.status || 'success');
                    reloadTableWithLoader(false);
                    if (response.data && response.data.draft_url) {
                        withCurrentListState(response.data.draft_url).then(function(nextUrl) {
                            window.location.href = nextUrl;
                        });
                    }
                })
                .fail(function(jqXHR, textStatus, error) {
                    showAlert(jqXHR.status, 'Request failed: ' + textStatus + '<br>' + error, 'error');
                });
        }

        function startGenerate(reportDatePrefill) {
            if (!validateSelection()) return;
            $('#generate_report_date').val('');
            if (reportDatePrefill) {
                $('#generate_report_date').val(moment(reportDatePrefill, 'YYYY-MM-DD').format(momentDateFormat));
            }
            $('#generate_report_modal').modal('show');
        }

        function checkAndGenerate(clientId, reportDate) {
            postWithLoader('<?= base_url('reports/daily/check-generate') ?>', {
                subject_id: clientId,
                report_date: reportDate
            }).done(function(check) {
                if (check.status === 'error' && check.statusText === 'NoSession') {
                    $('#generate_report_modal').modal('hide');
                    Swal.fire({
                        title: 'No Sessions',
                        text: 'No session exists for selected date.',
                        icon: 'info',
                        customClass: {
                            confirmButton: 'btn btn-primary w-xs me-2 mt-2'
                        },
                        buttonsStyling: false
                    });
                    return;
                }

                if (check.status === 'error' && check.statusText === 'ActiveDraftExists') {
                    $('#generate_report_modal').modal('hide');
                    handleActiveDraftResponse(check);
                    return;
                }

                if (check.status !== 'success') {
                    showAlert(check.statusText || '', check.message || 'Generate check failed.', check.status || 'error');
                    return;
                }

                if (check.data && check.data.report_exists) {
                    Swal.fire({
                        title: 'Daily Report Exists',
                        text: 'Latest version is v' + check.data.latest_version_no + '. Create the next draft version?',
                        icon: 'warning',
                        showCancelButton: true,
                        confirmButtonText: 'Create Draft',
                        cancelButtonText: 'Cancel',
                        customClass: {
                            confirmButton: 'btn btn-primary w-xs me-2 mt-2',
                            cancelButton: 'btn btn-secondary w-xs mt-2'
                        },
                        buttonsStyling: false
                    }).then(function(result) {
                        if (result.isConfirmed) {
                            runGenerate(clientId, reportDate);
                        }
                    });
                    return;
                }

                runGenerate(clientId, reportDate);
            }).fail(function(jqXHR, textStatus, error) {
                showAlert(jqXHR.status, 'Request failed: ' + textStatus + '<br>' + error, 'error');
            });
        }

        function runDeleteLatest(versionId, reportId) {
            if (!versionId) return;
            postWithLoader('<?= base_url('reports/daily/version') ?>/' + versionId + '/delete', {})
                .done(function(response) {
                    if (response.status !== 'success') {
                        showAlert(response.statusText || '', response.message || 'Unable to delete latest version.', response.status || 'error');
                        return;
                    }

                    showAlert(response.statusText || '', response.message || 'Deleted.', response.status || 'success');
                    reloadTableWithLoader(false);

                    var reportDeleted = response.data && response.data.report_deleted;
                    if (reportDeleted) {
                        activeVersionsReportId = 0;
                        activeVersionsDateLabel = '';
                        $('#versions_modal').modal('hide');
                        return;
                    }

                    if (activeVersionsReportId && reportId && activeVersionsReportId === reportId && $('#versions_modal').hasClass('show')) {
                        loadVersions(activeVersionsReportId, activeVersionsDateLabel);
                    }
                })
                .fail(function(jqXHR, textStatus, error) {
                    showAlert(jqXHR.status, 'Request failed: ' + textStatus + '<br>' + error, 'error');
                });
        }

        function runDeleteAll(reportId) {
            if (!reportId) return;
            postWithLoader('<?= base_url('reports/daily/report') ?>/' + reportId + '/delete-all', {})
                .done(function(response) {
                    if (response.status !== 'success') {
                        showAlert(response.statusText || '', response.message || 'Unable to delete all versions.', response.status || 'error');
                        return;
                    }

                    showAlert(response.statusText || '', response.message || 'Deleted.', response.status || 'success');
                    activeVersionsReportId = 0;
                    activeVersionsDateLabel = '';
                    $('#versions_modal').modal('hide');
                    reloadTableWithLoader(false);
                })
                .fail(function(jqXHR, textStatus, error) {
                    showAlert(jqXHR.status, 'Request failed: ' + textStatus + '<br>' + error, 'error');
                });
        }

        function loadVersions(reportId, dateLabel, reportDate) {
            activeVersionsReportId = parseInt(reportId, 10) || 0;
            activeVersionsDateLabel = dateLabel || '';
            activeVersionsReportDate = reportDate || '';
            $('#versions_modal_title').text('Report Versions [' + activeVersionsDateLabel + ']');

            postWithLoader('<?= base_url('reports/daily/versions') ?>', { report_id: activeVersionsReportId })
                .done(function(response) {
                    if (response.status !== 'success') {
                        showAlert(response.statusText, response.message, response.status);
                        return;
                    }

                    var rows = response.data || [];
                    var html = '';
                    rows.forEach(function(row, idx) {
                        var isLatest = idx === 0;
                        var actionParts = [];

                        if (row.status === 'DRAFT') {
                            actionParts.push('<a class="btn btn-sm btn-soft-primary open-draft-link" href="#" data-version-id="' + row.version_id + '"><i class="ri-draft-line me-1"></i>Open Draft</a>');
                        } else if (row.status === 'FINAL' && row.artifact_id) {
                            actionParts.push('<a target="_blank" class="btn btn-sm btn-soft-danger" href="<?= base_url('reports/daily/version') ?>/' + row.version_id + '/pdf"><i class="ri-file-pdf-line me-1"></i>View PDF</a>');
                        }

                        if (row.status === 'FINAL' && canGenerateReport) {
                            actionParts.push('<button type="button" class="btn btn-sm btn-soft-info regenerate-version" data-version-id="' + row.version_id + '" data-report-date="' + activeVersionsReportDate + '"><i class="ri-refresh-line me-1"></i>Regenerate</button>');
                        }

                        if (canDeleteVersion && isLatest) {
                            actionParts.push('<button type="button" class="btn btn-sm btn-soft-danger delete-latest-version" data-version-id="' + row.version_id + '" data-report-id="' + activeVersionsReportId + '" data-version-label="v' + row.version_no + '" data-version-status="' + row.status + '"><i class="ri-delete-bin-line me-1"></i>Delete</button>');
                        }

                        var actionHtml = actionParts.length > 0
                            ? '<div class="versions-action-wrap d-flex flex-wrap gap-1">' + actionParts.join('') + '</div>'
                            : '-';

                        html += '<tr>' +
                            '<td>v' + row.version_no + '</td>' +
                            '<td>' + row.status + '</td>' +
                            '<td>' + (row.generated_at_display || row.generated_at || '-') + '</td>' +
                            '<td>' + (row.generated_by_name || '-') + '</td>' +
                            '<td>' + actionHtml + '</td>' +
                            '</tr>';
                    });

                    if (html === '') {
                        html = '<tr><td colspan="5" class="text-center">No versions found.</td></tr>';
                    }

                    $('#versions_table tbody').html(html);
                    $('#versions_modal').modal('show');
                })
                .fail(function(jqXHR, textStatus, error) {
                    showAlert(jqXHR.status, 'Request failed: ' + textStatus + '<br>' + error, 'error');
                });
        }

